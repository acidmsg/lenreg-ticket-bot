/**
 * Возвращает HTML-строку с инлайн SVG-иконкой Lucide.
 *
 * @param {string} name — имя иконки (ключ в SVG_CONTENT)
 * @param {number} [size=20] — размер иконки в px
 * @returns {string} HTML-строка SVG или '', если иконка не найдена
 */
